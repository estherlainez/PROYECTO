
public class RedesSociales {

	String nombre;
	String Nick;
	
	public RedesSociales(String no, String ni) {
		this.nombre=no;
		this.Nick=ni;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getNick() {
		return Nick;
	}

	public void setNick(String nick) {
		Nick = nick;
	}
}
